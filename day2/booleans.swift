// Boolean -> true or false

let isMultiple = 120.isMultiple(of: 3)
print(isMultiple)

// ! not operator

var isAuthentificated = false

isAuthentificated = !isAuthentificated
print(isAuthentificated)

isAuthentificated = !isAuthentificated
print(isAuthentificated)

// toggle() to flip bool value

var gameOver = false
print("Game Over: \(gameOver)")

gameOver.toggle()
print("Game Over: \(gameOver)")